num1 = 100
num2 = 95
num3 = 90

qualifying_time = input("what was your qualifying time?")
print(qualifying_time + " minutes")

if int(qualifying_time) > num1:
    print("No award")

elif int(qualifying_time) <= num1 != num2:
    print("Provincial Colours")

elif int(qualifying_time) > num2  <= num1:
    print("Provincial Half Colours")

elif int(qualifying_time) >= num3 < num2:
    print("Provincial Scroll")
